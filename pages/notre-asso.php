	<section class="content-association conditions-association" id="conditions-creation">
		<h1>Les conditions de création</h1>
		<div class="content-wrapper">
			<h3>Choisir un nom :</h3>
			<ul>
				<li>Il faut s'assurer que le nom choisie n’est pas déjà utilisé par une autre association pour pouvoir s’en servir.</li>
				<li>Savoir si le nom de l’association choisi n’est pas protégé comme par exemple un nom de marque qui à été déposé à lNPI.</li>
				<li>Connaître et possédait un adresse permettant d'installer les bureaux de l'association.</li>
				<li>Suite à cela, il faut demander et avoir l'approbation du déparrtement pour que l’association soit déclarée.</li>
				<li>Il est strictement obligatoire d’avoir des idéaux non raciste ou antisémites, aucune maltraitance envers autrui, ne faisant pas allusion à tout autre état de vie de personne de la société dans le nom ou dans les dires.</li>
				<li>L’association doit avoir un minimum de 2 partisans pour que celle-ci soit déclarée.</li>
				<li>De plus, si l’association est à but lucratif ou non, aucune démarche illégale ou transaction illicite n’est autorisée.</li>
			</ul>
		</div>
		<div class="content-wrapper">
			<h3>Avoir un statut d’association :</h3>
			<p><u>Les statuts sont l’acte fondateur d’une association. Il s’agit d’un contrat signé par un minimum de 2 personnes.</u></p>
			<p>Les statuts sont l’acte de naissance de l’association. Ils comportent les informations décrivant l'objet (ou le but) de l'association et ses règles de fonctionnement. Les statuts peuvent être complétés par un règlement intérieur.<br><br>Les éléments qui doivent figurer dans les statuts de l’association sont, <b>le titre de l’association, les conditions d’admission des membres et des règles d’organisation/fonctionnement</b>.<br>De plus, les statuts doivent respecter des règles pour pouvoir se conformer à des statuts types, les soumettre à une autorité de tutelle comme un ministère et pouvoir mentionner les services fournis.<br><br>On doit déclarer les événements ou activités comme les modifications de statuts et le changement au niveau de l’administration interne à l’association. Mais, comme nous le savons, toute les associations manquent de main d'œuvre, c’est pour cela qu’il existe un formulaire de recrutement et aussi des formations a porté de tous, l’argent récolté par les formations iront directement dans l’investissement et l’achat de matériels canin.<br>Puis, si vous souhaitez juste faire des dons, vous recevez suite à cela une réduction d'impôt ayant pour but de faire participer l'État. Si toutefois vous voulez nous aider encore plus, il existe aussi des congé de responsable d’association bénévole vous autorisant en contrepartie d’aider une association, d’avoir des congés non rémunéré.</p>
		</div>

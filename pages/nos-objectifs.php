		<h1>Notre engagement</h1>
		<div class="content-wrapper">
			<p>Notre engagement consiste à nous rapprocher des animaux pour mieux en prendre soin et mieux les comprendre.</p>
			<p>Notre engagement comprend également une réintégration de nos animaux arrivant en refuge le plus rapidement possible dans un nouveaux foyer leurs convenant au mieux et adapté à leurs besoins.</p>
			<p>Également, nous nous engageons à soigner nos amis à 4 pattes lorsque ceux-ci en ont besoin. Un suivie de l'ensemble de nos pensionnaires qu'il soit en refuge ou chez leurs nouveaux propriétaires est également assurée par nos bénévoles.</p>
			<p>Un suivi comportemental avec des tests de socialisation avec les autres animaux, notamment les chats et les autres chiens ainsi qu'avec l'humain est surveillé de très près. Nous assurons également le dressage de ceux-ci si nécessaire.</p>
			<p>La recherche d'un propriétaire ayant les moyens, la capacité et l'amour pour son nouveau compagnon est validée lors de l'adoption. Nous pratiquons une adoption responsable.</p>
